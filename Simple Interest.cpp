// User function Template for C++
class Solution {
public:
    double simpleInterest(int P, int R, int T) {
        
        double p = P;
        return (p * T * R) / 100;
    }
}; 

// This class provides a function to calculate simple interest.
// It converts the principal amount to double for accurate calculation.
// Then, it calculates simple interest using the formula: (P * T * R) / 100,
// where P is the principal amount, T is the time period, and R is the rate of interest.

