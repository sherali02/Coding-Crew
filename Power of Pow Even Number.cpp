class Solution
{
public:
    long long int sum_of_square_evenNumbers(long long int n)
    {
        return n * (n + 1) * (2 * n + 1) * 2 / 3;
    }
}; 

// This class provides a function to calculate the sum of squares of even numbers up to n.
// It utilizes a formula that efficiently computes the sum of squares of even numbers.
// The formula is n * (n + 1) * (2 * n + 1) * 2 / 3.
// Finally, it returns the calculated sum of squares of even numbers.

